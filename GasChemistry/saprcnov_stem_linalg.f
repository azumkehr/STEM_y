      SUBROUTINE KppDecomp(N,V,IER)

      INCLUDE 'saprcnov_stem_s.h'

      REAL*8 V(LU_NONZERO_V), W(NSPEC)
      INTEGER      k, kk, j, jj
      REAL*8 a 

      IER = 0
      DO k=1,N
        IF ( V( LU_DIAG_V(k) ) .EQ. 0. ) THEN
            IER = k
            RETURN
        END IF
        DO kk = LU_CROW_V(k), LU_CROW_V(k+1)-1
              W( LU_ICOL_V(kk) ) = V(kk)
        END DO
        DO kk = LU_CROW_V(k), LU_DIAG_V(k)-1
            j = LU_ICOL_V(kk)
            a = -W(j) / V( LU_DIAG_V(j) )
            W(j) = -a
            DO jj = LU_DIAG_V(j)+1, LU_CROW_V(j+1)-1
               W( LU_ICOL_V(jj) ) = W( LU_ICOL_V(jj) ) + a*V(jj)
            END DO
         END DO
         DO kk = LU_CROW_V(k), LU_CROW_V(k+1)-1
            V(kk) = W( LU_ICOL_V(kk) )
         END DO
      END DO
      RETURN
      END
